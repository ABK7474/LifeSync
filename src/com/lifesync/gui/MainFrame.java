    private void antrenmaniTamamlaIslemi() {
        int seciliSatir = tblSporcuAntrenmanlar.getSelectedRow();
        if (seciliSatir == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Lütfen tamamlamak için bir antrenman seçin.", "Uyarı", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int antrenmanId = (int) sporcuAntrenmanTableModel.getValueAt(seciliSatir, 0);
        String durum = (String) sporcuAntrenmanTableModel.getValueAt(seciliSatir, 3);
        
        if (durum.equals("Tamamlandı")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Bu antrenman zaten tamamlanmış.", "Bilgi", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        com.lifesync.Kullanici aktif = com.lifesync.AuthService.getAktifKullanici();
        if (aktif instanceof com.lifesync.Sporcu) {
            com.lifesync.Sporcu s = (com.lifesync.Sporcu) aktif;
            for (com.lifesync.Antrenman a : s.getAntrenmanListesi()) {
                if (a.getAntrenmanId() == antrenmanId) {
                    a.setTamamlandi(true);
                    com.lifesync.VeriDeposu.antrenmanlariDiskeYaz(); // Diske kaydet
                    sporcuAntrenmanTablosunuDoldur(); // Tabloyu yenile
                    javax.swing.JOptionPane.showMessageDialog(this, "Antrenman başarıyla tamamlandı!", "Tebrikler", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    break;
                }
            }
        }
    }

    private void sporcuAntrenmanTablosunuDoldur() {
        if (sporcuAntrenmanTableModel == null) return;
        sporcuAntrenmanTableModel.setRowCount(0);
        
        com.lifesync.Kullanici aktif = com.lifesync.AuthService.getAktifKullanici();
        if (aktif instanceof com.lifesync.Sporcu) {
            com.lifesync.Sporcu s = (com.lifesync.Sporcu) aktif;
            for (com.lifesync.Antrenman a : s.getAntrenmanListesi()) {
                sporcuAntrenmanTableModel.addRow(new Object[]{
                    a.getAntrenmanId(), 
                    a.getTarih().toString(), 
                    a.getAntrenmanTuru().name(), 
                    a.isTamamlandi() ? "Tamamlandı" : "Bekliyor"
                });
            }
        }
    }



