    private void btnGunlukKaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGunlukKaydetActionPerformed
        // TODO add your handling code here:
        // 1. Arayüzden ham metin verilerini çekme
        String alinanStr = txtAlinanKalori.getText();
        String harcananStr = txtHarcananKalori.getText();

        // 2. Boş alan kontrolü (Early Return)
        if (alinanStr.trim().isEmpty() || harcananStr.trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Değerler boş bırakılamaz. Lütfen 0 veya daha büyük bir sayısal değer girin.", "Uyarı", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 3. Tip Güvenliği ve Matematiksel İşlem Blokları
        try {
            double alinanKalori = Double.parseDouble(alinanStr);
            double harcananKalori = Double.parseDouble(harcananStr);

            if (alinanKalori < 0 || harcananKalori < 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Kalori değerleri negatif olamaz.", "Mantık Hatası", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }

            // 4. Bellekteki aktif kullanıcı nesnesini çağırma
            com.lifesync.Kullanici aktif = com.lifesync.AuthService.getAktifKullanici();

            // Nesnenin Sporcu sınıfından türeyip türemediğini doğrulama
            if (aktif instanceof com.lifesync.Sporcu) {
                com.lifesync.Sporcu sporcu = (com.lifesync.Sporcu) aktif;

                // Net enerji dengesi hesaplama algoritması
                double netKalori = alinanKalori - harcananKalori;
                String durum;

                if (netKalori > 0) {
                    durum = "Pozitif Denge (Kilo Alma Eğilimi)";
                } else if (netKalori < 0) {
                    durum = "Negatif Denge (Kilo Verme Eğilimi)";
                } else {
                    durum = "Nötr Denge (Kilo Koruma)";
                }

                // Arayüze sonuçları yansıtma
                lblGunlukOzet.setText("Net Kalori: " + netKalori + " kcal | Durum: " + durum);

                // TODO: İlerleyen aşamada burada projedeki 'GunlukTakip' nesnesi oluşturulup,
                // sporcu.getGunlukTakipListesi().add(yeniGunlukTakip); şeklinde RAM'e eklenecek
                // ve AuthService.verileriKaydet() metodu ile TXT dosyasına yazılacaktır.

                // Metin kutularını sonraki giriş için temizleme
                txtAlinanKalori.setText("");
                txtHarcananKalori.setText("");

                javax.swing.JOptionPane.showMessageDialog(this, "Günlük takip verileri başarıyla işlendi.", "İşlem Başarılı", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Bu işlem yalnızca sporcu profilleri içindir.", "Yetki Hatası", javax.swing.JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException ex) {
            // Harf veya özel karakter girilmesi durumunda çökme önleyici
            javax.swing.JOptionPane.showMessageDialog(this, "Kritik Hata: Kalori alanlarına yalnızca sayısal değerler girilmelidir.", "Veri Tipi Hatası", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGunlukKaydetActionPerformed

