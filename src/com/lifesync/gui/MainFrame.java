            sb.append("--- Antrenman ---\n");
            sb.append("Toplam Antrenman: ").append(toplam).append("\n");
            sb.append("Tamamlanan: ").append(tamamlanan).append("\n");
            sb.append("Bekleyen: ").append(toplam - tamamlanan).append("\n");
            if (toplam > 0) {
                sb.append("Tamamlanma Orani: %").append(String.format("%.0f", (tamamlanan * 100.0 / toplam))).append("\n");
            }
            sb.append("\nAntrenman Detaylari:\n");
